// Function to determine component type from name
function getComponentTypeFromName(name) {
  const lowerName = name.toLowerCase();
  
  // CPU detection
  if (lowerName.includes('ryzen') || lowerName.includes('core i') || lowerName.includes('intel') || lowerName.includes('amd') || lowerName.includes('cpu') || lowerName.includes('r3') || lowerName.includes('r5') || lowerName.includes('r7') || lowerName.includes('r9')) {
    return 'cpu';
  }
  
  // GPU detection
  if (lowerName.includes('rtx') || lowerName.includes('gtx') || lowerName.includes('rx') || lowerName.includes('graphics') || lowerName.includes('gpu') || lowerName.includes('radeon')) {
    return 'gpu';
  }
  
  // RAM detection
  if (lowerName.includes('ddr') || lowerName.includes('memory') || lowerName.includes('ram') || lowerName.includes('fury') || lowerName.includes('vengeance') || lowerName.includes('trident')) {
    return 'ram';
  }
  
  // Motherboard detection
  if (lowerName.includes('motherboard') || lowerName.includes('mobo') || lowerName.includes('b550') || lowerName.includes('b660') || lowerName.includes('h510') || lowerName.includes('x570') || lowerName.includes('asrock') || lowerName.includes('asus') || lowerName.includes('msi') || lowerName.includes('gigabyte')) {
    return 'motherboard';
  }
  
  // Storage detection
  if (lowerName.includes('ssd') || lowerName.includes('hdd') || lowerName.includes('nvme') || lowerName.includes('samsung') || lowerName.includes('wd') || lowerName.includes('seagate') || lowerName.includes('crucial') || lowerName.includes('kingston')) {
    return 'storage';
  }
  
  // PSU detection
  if (lowerName.includes('psu') || lowerName.includes('power') || lowerName.includes('watt') || lowerName.includes('corsair') || lowerName.includes('evga') || lowerName.includes('seasonic') || lowerName.includes('cooler master')) {
    return 'psu';
  }
  
  // Case detection
  if (lowerName.includes('case') || lowerName.includes('chassis') || lowerName.includes('nzxt') || lowerName.includes('phanteks') || lowerName.includes('fractal') || lowerName.includes('inplay') || lowerName.includes('keytech')) {
    return 'case';
  }
  
  // Cooler detection
  if (lowerName.includes('cooler') || lowerName.includes('fan') || lowerName.includes('noctua') || lowerName.includes('corsair') || lowerName.includes('arctic') || lowerName.includes('aio') || lowerName.includes('liquid')) {
    return 'cooler';
  }
  
  return 'default';
}

export function getComponentImage(name, componentType = null) {
  // First, try to get the exact image for the component name
  if (componentImages[name]) {
    return componentImages[name];
  }
  
  // If no exact match, determine type from name and use fallback
  const type = componentType || getComponentTypeFromName(name);
  const images = fallbackImages[type] || fallbackImages.default;
  
  // Return a random image from the array
  return images[Math.floor(Math.random() * images.length)];
} 